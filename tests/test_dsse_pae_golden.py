# ===========================================================================
# 1. PAE golden vectors — INDEPENDENTLY SOURCED from the DSSE spec +
#    securesystemslib reference test suite, NOT generated by pae.py.
#
# Source: https://github.com/in-toto/dsse/blob/master/spec.md §PAE
#         https://github.com/secure-systems-lab/securesystemslib/blob/main/tests/test_dsse.py
#
# The spec defines:
#   PAE(type, body) = "DSSEv1" SP DEC(LEN(type)) SP type SP DEC(LEN(body)) SP body
#
# where LEN is the BYTE count of the UTF-8 encoding.
# ===========================================================================

# Vector 1: the canonical DSSE spec / securesystemslib example.
#   payloadType = "http://example.com/HelloWorld"  (29 UTF-8 bytes)
#   payload     = b"hello world"                    (11 bytes)
#   PAE         = b"DSSEv1 29 http://example.com/HelloWorld 11 hello world"
#
# Note: "http://example.com/HelloWorld" is 29 bytes (29 ASCII chars).
# A circulating variant of the spec text shows "28" but the reference
# securesystemslib test suite and the in-toto spec.md both use 29.
# We pin the securesystemslib-confirmed value.
_SPEC_PAYLOAD_TYPE = "http://example.com/HelloWorld"
_SPEC_PAYLOAD = b"hello world"
_SPEC_PAE_EXPECTED: bytes = b"DSSEv1 29 http://example.com/HelloWorld 11 hello world"


def test_pae_spec_vector() -> None:
    """PAE matches the DSSE spec / securesystemslib reference example."""
    result = pae(_SPEC_PAYLOAD_TYPE, _SPEC_PAYLOAD)
    assert result == _SPEC_PAE_EXPECTED, (
        f"PAE mismatch.\n  got:      {result!r}\n  expected: {_SPEC_PAE_EXPECTED!r}"
    )


# ---------------------------------------------------------------------------
# Vector 2: empty payload.
#   payloadType = "application/vnd.test"  (20 ASCII bytes)
#   payload     = b""                      (0 bytes)
#   PAE         = b"DSSEv1 20 application/vnd.test 0 "
#
# Hand-computed:
#   "DSSEv1" + " " + "20" + " " + "application/vnd.test" + " " + "0" + " " + ""
# ---------------------------------------------------------------------------
_EMPTY_PAYLOAD_TYPE = "application/vnd.test"
_EMPTY_PAYLOAD = b""
_EMPTY_PAE_EXPECTED: bytes = b"DSSEv1 20 application/vnd.test 0 "


def test_pae_empty_payload() -> None:
    """PAE with empty payload matches hand-computed vector."""
    result = pae(_EMPTY_PAYLOAD_TYPE, _EMPTY_PAYLOAD)
    assert result == _EMPTY_PAE_EXPECTED, (
        f"PAE mismatch.\n  got:      {result!r}\n  expected: {_EMPTY_PAE_EXPECTED!r}"
    )


# ---------------------------------------------------------------------------
# Vector 3: UTF-8 multibyte payloadType — proves LEN is BYTE count, not char count.
#   payloadType = "application/café"
#     = "application/caf" (15 ASCII chars) + "é" (U+00E9, 2 UTF-8 bytes)
#     = 15 + 2 = 17 UTF-8 bytes, but only 16 Unicode code points.
#   payload     = b"data"  (4 bytes)
#   PAE         = b"DSSEv1 17 application/caf\xc3\xa9 4 data"
#
# Hand-computed:
#   "application/café".encode("utf-8") = b"application/caf\xc3\xa9"  (17 bytes)
#   LEN = 17 (bytes), NOT 16 (chars)
# ---------------------------------------------------------------------------
_MULTIBYTE_PAYLOAD_TYPE = "application/café"  # 16 chars, 17 UTF-8 bytes
_MULTIBYTE_PAYLOAD = b"data"
_MULTIBYTE_PAE_EXPECTED: bytes = b"DSSEv1 17 application/caf\xc3\xa9 4 data"


def test_pae_utf8_multibyte_byte_length() -> None:
    """PAE uses byte length, not char count, for multibyte UTF-8 payloadType."""
    result = pae(_MULTIBYTE_PAYLOAD_TYPE, _MULTIBYTE_PAYLOAD)
    # The framing integer MUST be 17 (bytes), not 16 (chars).
    assert result == _MULTIBYTE_PAE_EXPECTED, (
        f"PAE mismatch.\n  got:      {result!r}\n  expected: {_MULTIBYTE_PAE_EXPECTED!r}"
    )
    # Extra guard: the framing number in the PAE is exactly "17", not "16".
    assert b" 17 " in result, "Expected byte-length 17 in PAE framing"
    assert b" 16 " not in result, "Char-count 16 must NOT appear in PAE framing"


def test_pae_empty_payload_type_raises() -> None:
    """pae() raises ValueError for an empty payloadType."""
    with pytest.raises(ValueError):
        pae("", b"some payload")


