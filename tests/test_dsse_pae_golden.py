# 3. kid_from_raw32 — cross-checked vs `cryptography` raw encoding
#
# Independence: the test imports cryptography to derive the raw 32-byte public
# key via an independent code path.  The production kid_from_raw32 uses ONLY
# stdlib (hashlib + base64).  The test asserts that both paths produce the
# same kid for a fixed seed.
#
# Hard-coded expected kid (computed once by running this test and pinning the
# result; the value is cross-validated by the cryptography path below):
#   seed  = bytes(range(32))  # 0x00..0x1f
#   raw32 = Ed25519PrivateKey.from_private_bytes(seed).public_key()
#           .public_bytes(Encoding.Raw, PublicFormat.Raw)
#         = 03a107bff3ce10be1d70dd18e74bc09967e4d6309ba50d5f1ddc8664125531b8
#   sha256(raw32)
#         = 56475aa75463474c0285df5dbf2bcab73da651358839e9b77481b2eab107708c
#   kid   = base64url_nopad(sha256(raw32))
#         = "Vkdap1RjR0wChd9dvyvKtz2mUTWIOem3dIGy6rEHcIw"
# ===========================================================================

_KID_TEST_SEED: bytes = bytes(range(32))
_KID_EXPECTED_RAW32_HEX = (
    "03a107bff3ce10be1d70dd18e74bc09967e4d6309ba50d5f1ddc8664125531b8"
)
_KID_EXPECTED = "Vkdap1RjR0wChd9dvyvKtz2mUTWIOem3dIGy6rEHcIw"


def test_kid_from_raw32_cross_checked() -> None:
    """kid_from_raw32 matches cryptography-derived raw32 + independent sha256+b64url.

    The `cryptography` library is used HERE (in the test) as an independent
    implementation to derive raw32 from a fixed seed.  The production
    kid_from_raw32 does NOT import cryptography.
    """
    from cryptography.hazmat.primitives.asymmetric.ed25519 import Ed25519PrivateKey
    from cryptography.hazmat.primitives.serialization import Encoding, PublicFormat

    # Derive raw32 via cryptography (independent path).
    priv = Ed25519PrivateKey.from_private_bytes(_KID_TEST_SEED)
    raw32 = priv.public_key().public_bytes(Encoding.Raw, PublicFormat.Raw)

    # Confirm raw32 matches our pinned hex (guards against seed/key-derivation drift).
    assert raw32.hex() == _KID_EXPECTED_RAW32_HEX, (
        f"raw32 mismatch:\n  got:      {raw32.hex()}\n  expected: {_KID_EXPECTED_RAW32_HEX}"
    )
    assert len(raw32) == 32

    # Compute the kid independently in the test using stdlib only.
    digest_independent = hashlib.sha256(raw32).digest()
    kid_independent = (
        base64.urlsafe_b64encode(digest_independent).rstrip(b"=").decode("ascii")
    )

    # Assert that the production kid_from_raw32 matches.
    kid_production = kid_from_raw32(raw32)
    assert kid_production == kid_independent, (
        f"kid mismatch:\n  production: {kid_production}\n  independent: {kid_independent}"
    )

    # Assert against the hard-coded pinned expected value.
    assert kid_production == _KID_EXPECTED, (
        f"kid mismatch vs pinned expected:\n"
        f"  got:      {kid_production}\n"
        f"  expected: {_KID_EXPECTED}"
    )


def test_kid_from_raw32_rejects_non_32_bytes() -> None:
    """kid_from_raw32 raises ValueError for inputs that are not exactly 32 bytes."""
    with pytest.raises(ValueError, match="32 bytes"):
        kid_from_raw32(b"")
    with pytest.raises(ValueError, match="32 bytes"):
        kid_from_raw32(bytes(31))
    with pytest.raises(ValueError, match="32 bytes"):
        kid_from_raw32(bytes(33))


def test_kid_no_padding() -> None:
    """kid_from_raw32 output never contains '=' padding."""
    raw32 = bytes(32)  # all zeros
    kid = kid_from_raw32(raw32)
    assert "=" not in kid


