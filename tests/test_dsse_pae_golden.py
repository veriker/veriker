# 6. Import-cleanliness probe — production modules must NOT have imported
#    cryptography, jcs, or rfc8785.
#
# This test confirms that importing audit_bundle.dsse.pae and
# audit_bundle.dsse.version_map does NOT pull in any of the forbidden
# heavy-dep modules.  (Cryptography IS imported in this test file itself
# as an independent cross-check, but that import happens AFTER this probe.)
# ===========================================================================


def test_production_modules_did_not_import_forbidden_deps() -> None:
    """audit_bundle.dsse.pae and version_map must not transitively import cryptography, jcs, or rfc8785.

    NOTE: This test does NOT assert that cryptography is absent from
    sys.modules globally (it is imported in test_kid_from_raw32_cross_checked
    as the independence anchor for the kid vector).  It asserts that the
    production module objects themselves did not introduce those imports.
    """
    # Check that neither pae.py nor version_map.py carries the forbidden attrs.
    forbidden = {"cryptography", "jcs", "rfc8785"}
    for mod in (_pae_mod, _vm_mod):
        for name in forbidden:
            assert not hasattr(mod, name), (
                f"Production module {mod.__name__} has attribute {name!r} — "
                "forbidden dep leaked into production module namespace"
            )

    # The production module's __dict__ must not contain the forbidden module objects.
    for mod in (_pae_mod, _vm_mod):
        for name, val in mod.__dict__.items():
            if isinstance(val, types.ModuleType):
                assert val.__name__.split(".")[0] not in forbidden, (
                    f"Production module {mod.__name__} imported {val.__name__!r} "
                    "(or a submodule of a forbidden dep) at the top level"
                )
