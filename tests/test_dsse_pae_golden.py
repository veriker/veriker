# 2. base64url no-pad helpers — round-trip + padding-rejection
# ===========================================================================


def test_b64url_encode_decode_roundtrip() -> None:
    """encode then decode returns original bytes for various lengths."""
    for n in range(16):  # cover all four remainder classes mod 4
        original = bytes(range(n))
        encoded = b64url_nopad_encode(original)
        assert "=" not in encoded, f"Padding found in encoded output for n={n}"
        decoded = b64url_nopad_decode(encoded)
        assert decoded == original, f"Round-trip failed for n={n}"


def test_b64url_encode_no_padding() -> None:
    """Output of b64url_nopad_encode never contains '='."""
    for n in range(64):
        encoded = b64url_nopad_encode(bytes(range(n)))
        assert "=" not in encoded


def test_b64url_decode_rejects_invalid_length() -> None:
    """b64url_nopad_decode rejects strings with remainder 1 mod 4 (always corrupt)."""
    # Length 1: "A" → remainder 1 → invalid
    with pytest.raises(ValueError, match="remainder 1"):
        b64url_nopad_decode("A")
    # Length 5: "AAAAA" → remainder 1 → invalid
    with pytest.raises(ValueError, match="remainder 1"):
        b64url_nopad_decode("AAAAA")


def test_b64url_decode_accepts_valid_lengths() -> None:
    """Strings with valid lengths (remainder 0, 2, 3 mod 4) decode successfully."""
    # remainder 0: "AAAA" → 3 bytes
    assert b64url_nopad_decode("AAAA") == b"\x00\x00\x00"
    # remainder 2: "AA" → 1 byte
    assert b64url_nopad_decode("AA") == b"\x00"
    # remainder 3: "AAA" → 2 bytes
    assert b64url_nopad_decode("AAA") == b"\x00\x00"


def test_b64url_nopad_known_vector() -> None:
    """b64url_nopad_encode matches known base64url no-pad encoding."""
    # SHA-256 of b"\x00" * 32 as a known anchor
    raw = bytes(32)
    digest = hashlib.sha256(raw).digest()
    expected = base64.urlsafe_b64encode(digest).rstrip(b"=").decode("ascii")
    assert b64url_nopad_encode(digest) == expected
    assert "=" not in expected


