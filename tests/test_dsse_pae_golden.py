# 5. payload_type_nfc — NFC normalization golden vector
#
# Vector: NFD "application/tést" (e + combining acute accent, 2 code
#         points for the 'é' position) → NFC "application/tést" (precomposed
#         U+00E9, 1 code point for 'é').
#
# These two strings are Unicode-canonically equivalent but differ at the
# byte level: NFD encodes the 'é' as 0x65 0xcc 0x81 (3 bytes) while NFC
# encodes it as 0xc3 0xa9 (2 bytes).
#
# Source: Unicode Standard §3.7 (Decomposition) + §3.11 (NFC/NFD).
# ===========================================================================

# NFD form: 'e' (U+0065) + combining acute accent (U+0301)
_NFC_INPUT_NFD = "application/tést"  # 18 code points (the 'é' is split)
# NFC form: precomposed 'é' (U+00E9)
_NFC_EXPECTED_NFC = "application/tést"  # 16 code points


def test_payload_type_nfc_normalizes_nfd_to_nfc() -> None:
    """payload_type_nfc converts NFD to NFC form."""
    result = payload_type_nfc(_NFC_INPUT_NFD)
    assert result == _NFC_EXPECTED_NFC, (
        f"NFC normalization failed:\n  got:      {result!r}\n  expected: {_NFC_EXPECTED_NFC!r}"
    )


def test_payload_type_nfc_idempotent() -> None:
    """Applying payload_type_nfc twice is the same as applying it once."""
    once = payload_type_nfc(_NFC_INPUT_NFD)
    twice = payload_type_nfc(once)
    assert once == twice


def test_payload_type_nfc_nfd_bytes_differ_from_nfc() -> None:
    """NFD and NFC forms have different byte representations (proves the test is non-trivial)."""
    nfd_bytes = _NFC_INPUT_NFD.encode("utf-8")
    nfc_bytes = _NFC_EXPECTED_NFC.encode("utf-8")
    assert nfd_bytes != nfc_bytes, "Test is degenerate: NFD and NFC bytes must differ"
    # NFD encodes é as e (0x65) + combining acute (0xcc 0x81) = 3 bytes
    # NFC encodes é as precomposed (0xc3 0xa9) = 2 bytes
    assert b"\xc3\xa9" in nfc_bytes
    assert b"\xcc\x81" in nfd_bytes


def test_payload_type_nfc_ascii_passthrough() -> None:
    """ASCII-only payloadType is unchanged by NFC normalization."""
    ascii_uri = "https://vkernel.dev/types/bundle.v1"
    assert payload_type_nfc(ascii_uri) == ascii_uri


