"""Golden-vector tests for audit_bundle.dsse — pure PAE preimage layer.

Independence discipline
-----------------------
PAE vectors are sourced from the in-toto DSSE spec and the securesystemslib
reference test suite, NOT generated by the production pae.py under test.

  Canonical source: https://github.com/in-toto/dsse/blob/master/spec.md
  Reference impl:   https://github.com/secure-systems-lab/securesystemslib

The kid vector is cross-checked using the `cryptography` library as an
independent implementation of raw-key extraction: the test derives
raw32 via `Ed25519PrivateKey.from_private_bytes(...).public_key()
.public_bytes(Encoding.Raw, PublicFormat.Raw)`, hashes it with stdlib
hashlib, and asserts that `kid_from_raw32` produces the same result.
`cryptography` is only imported in this test module, NEVER in production
pae.py or version_map.py.
"""

